package io.themis.column;

/**
 * Represents boolean values.
 */
public class BooleanColumn extends Column {

    public BooleanColumn(int id, String name, boolean required) {
        super(id, name, "boolean", required);
    }

    @Override
    public <T> T accept(ColumnVisitor<T> visitor) {
        return visitor.visit(this);
    }

    @Override
    public String toString() {
        final StringBuilder sb = new StringBuilder("BooleanColumn{");
        sb.append("id=").append(id());
        sb.append(", name='").append(name()).append('\'');
        sb.append(", type='").append(type()).append('\'');
        sb.append(", required=").append(required());
        sb.append('}');
        return sb.toString();
    }
}
